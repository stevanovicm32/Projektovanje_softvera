    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int row = jTableKlijenti.getSelectedRow();
        
        if(row>=0){
            Klijent k = ((TableModelKlijenti) jTableKlijenti.getModel()).getSelectedKlijent(row);
            int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                    + "obrisete ovog klijenta?" + k.toString(), "Konfirmacija", JOptionPane.YES_NO_OPTION);

            if (result == JOptionPane.NO_OPTION) {
                return;
            }

            if (result == JOptionPane.YES_OPTION) {
                try {
                    KlijentskiKontroler.getInstance().deleteKlijent(k);
                    this.refreshTable();
                    JOptionPane.showMessageDialog(this, "Uspesno obrisan klijent.");
                } catch (Exception ex) {
                    Logger.getLogger(FormaPretragaKlijenta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Morate odabrati klijenta!", "Obavestenje!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

