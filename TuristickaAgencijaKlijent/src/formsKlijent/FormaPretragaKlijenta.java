    private void jButtonDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetaljiActionPerformed
        int row = jTableKlijenti.getSelectedRow();
        int signal = 1;

        if (row >= 0) {
            Klijent k = ((TableModelKlijenti) jTableKlijenti.getModel()).getSelectedKlijent(row);
            new FormaKlijent(this, true, k, signal).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Morate odabrati klijenta!", "Obavestenje!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDetaljiActionPerformed

