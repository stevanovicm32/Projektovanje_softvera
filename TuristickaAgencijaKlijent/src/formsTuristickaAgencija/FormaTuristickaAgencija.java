    private void jButtonIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniActionPerformed
        try {
            if(jTextFieldAdresa.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Greska!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String adresa = jTextFieldAdresa.getText();
            
            if(adresa.length()>100){
                JOptionPane.showMessageDialog(this, "Adresa mora imati manje od 50 karaktera!", "Greska!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            TuristickaAgencija ta = new TuristickaAgencija(pomocna.getIdAgencije(), pomocna.getNaziv(), adresa, pomocna.getUsername(), pomocna.getPassword());
            KlijentskiKontroler.getInstance().updateTuristickaAgencija(ta);
            JOptionPane.showMessageDialog(this, "Uspesno izmenjena turisticka agencija.", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(FormaTuristickaAgencija.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonIzmeniActionPerformed

    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
        try {
            VrstaUsluge vu = (VrstaUsluge) jComboBoxVrstaUsluge.getSelectedItem();
            TAVU t = new TAVU(null, pomocna, vu);
            
            KlijentskiKontroler.getInstance().addTAVU(t);
            JOptionPane.showMessageDialog(this, "Uspesno dodata vrsta usluge.", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            this.refreshTable();
        } catch (Exception ex) {
            Logger.getLogger(FormaTuristickaAgencija.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDodajActionPerformed

    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int row = jTableVrstaUsluge.getSelectedRow();
        
        if(row>=0){
            VrstaUsluge vu = ((TableModelVrstaUslugeTuristickaAgencija) jTableVrstaUsluge.getModel()).getSelectedVrstaUsluge(row);
            TAVU t = new TAVU(null, pomocna, vu);
            int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                    + "obrisete ovu vrstu usluge? " + vu.toString(), "Konfirmacija", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.NO_OPTION) {
                return;
            }

            if (result == JOptionPane.YES_OPTION) {
                try {
                    KlijentskiKontroler.getInstance().deleteTAVU(t);
                    this.refreshTable();
                    JOptionPane.showMessageDialog(this, "Uspesno obrisana vrsta usluge.");
                } catch (Exception ex) {
                    Logger.getLogger(FormaTuristickaAgencija.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, ex.toString(), "Greska!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Morate odabrati nacionalnost!", "Obavestenje!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

