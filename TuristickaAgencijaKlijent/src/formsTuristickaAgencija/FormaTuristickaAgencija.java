    private void popuniVrsteUsluge() {
    try {
        ArrayList<VrstaUsluge> vrste = KlijentskiKontroler.getInstance().getAllVrstaUsluge();
        Set<String> naziviUsluga = new HashSet<>();

        jComboBoxVrstaUsluge.removeAllItems();

        for (VrstaUsluge u : vrste) {
            if (!naziviUsluga.contains(u.getNazivUsluge())) { 
                naziviUsluga.add(u.getNazivUsluge());
                jComboBoxVrstaUsluge.addItem(u);
            }
        }
    } catch (Exception ex) {
        Logger.getLogger(FormaPretraziTuristickaAgencija.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    void refreshTable(){
        TableModelVrstaUslugeTuristickaAgencija tm = (TableModelVrstaUslugeTuristickaAgencija) jTableVrstaUsluge.getModel();
        tm.refreshTable();
    }
